class Rejilla:

    def __init__(self, fila, columna):

        self.fila = fila
        self.columna = columna

    def getFila(self):
        return self.fila

    def getColumna(self):
        return self.columna
    
    def setFila(self, fila):
        self.fila = fila

    def setColumna(self, columna):
        self.columna = columna    
